    private void HoraInicioMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_HoraInicioMousePressed
        if(HoraInicio.getText().equals("hh:mm")){
        HoraInicio.setText("");
        HoraInicio.setForeground(Color.BLACK);
            
        }
        if(String.valueOf(HoraFin.getText()).isEmpty()){
        HoraFin.setText("hh:mm");
        HoraFin.setForeground(Color.GRAY);
        }
        
        
        
    }//GEN-LAST:event_HoraInicioMousePressed

    private void HoraFinMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_HoraFinMousePressed
       if(String.valueOf(HoraFin.getText()).equals("hh:mm")){
        HoraFin.setText("");
        HoraFin.setForeground(Color.BLACK);
        
    }
       if(HoraInicio.getText().isEmpty()){
        HoraInicio.setText("hh:mm");
        HoraInicio.setForeground(Color.GRAY);
       }
        
        
        
        
    }//GEN-LAST:event_HoraFinMousePressed

