    private void addItemstoBox() {

        if (parkSelect == "S1") {
            for (int i = 0; i < 5; i++) {
                filaComboBox.addItem(String.valueOf(i + 1));
            }
            for (int i = 0; i < 4; i++) {
                columComboBox.addItem(String.valueOf(i + 1));
            }
        } else if (parkSelect == "S2") {
            for (int i = 0; i < 5; i++) {
                filaComboBox.addItem(String.valueOf(i + 1));
            }
            for (int i = 0; i < 5; i++) {
                columComboBox.addItem(String.valueOf(i + 1));
            }
        } else if (parkSelect == "S3") {
            for (int i = 0; i < 6; i++) {
                filaComboBox.addItem(String.valueOf(i + 1));
            }
            for (int i = 0; i < 5; i++) {
                columComboBox.addItem(String.valueOf(i + 1));
            }
        }
    }
