// Matriz de Botones
    private int rowS2 = 5; // 5 filas
    private int columS2 = 5; // 5 columnas
    JButton[][] parkS2;

    //metodo para cerrar
    private void cerrarVentana() {
        JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
        if (frame != null) {
            frame.dispose(); // Cierra la ventana
        }
    }

// Método para Crear Matriz
    //Parqueo S1 5x4
    public void setMatrizS2() {
        parkS2 = new JButton[rowS2][columS2];
        //Posicion inicial del primer boton
        int positionX = 30;
        int positionY = 30;

        //creacion de botones por coordenadas
        for (int i = 0; i < rowS2; i++) {
            for (int j = 0; j < columS2; j++) {
                parkS2[i][j] = new JButton();
                parkS2[i][j].setBounds(positionX, positionY, 115, 60);
                parkS2[i][j].setBackground(Color.LIGHT_GRAY);
                parkS2[i][j].setText("Loading...");
                ButtonController bt = new ButtonController();
                parkS2[i][j].addActionListener(bt);

                panelS2.add(parkS2[i][j]);
                positionX += 180;
            }
            positionX = 30;
            positionY += 120;
        }

        panelS2.revalidate();
        panelS2.repaint();
        valueBottons(rowS2, columS2, Data.matrizS2); // Llamar sin parámetros
    }

//Declarar la accion de los botones
    private class ButtonController implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {

            //seleccionar el boton de la matrix
            for (int i = 0; i < rowS2; i++) {
                for (int j = 0; j < columS2; j++) {
                    if (e.getSource().equals(parkS2[i][j])) {
                        if (Data.matrizS2[i][j] == "P") {
                            JOptionPane.showMessageDialog(null, "Elije otro espacio este no esta disponible");
                        } else {
                            formularioRequest form = new formularioRequest();
                            form.setDatoX(i);
                            form.setDatoY(j);
                            form.setStatus(Data.matrizS2[i][j]);
                            form.setidPark("S2");
                            cerrarVentana();
                            form.setVisible(true);
                        }
                    }
                }
            }
        }
    }

// Método para actualizar botones según la disponibilidad
    public void valueBottons(int x, int y, String[][] matrix) {
        for (int i = 0; i < x; i++) {
            for (int j = 0; j < y; j++) {
                if (matrix[i][j] == "O") {
                    parkS2[i][j].setBackground(Color.GREEN);
                    parkS2[i][j].setText("Disponible");
                } else if (matrix[i][j] == "E") {
                    parkS2[i][j].setBackground(Color.BLUE);
                    parkS2[i][j].setText("Disponible");
                } else if (matrix[i][j] == "D") {
                    parkS2[i][j].setBackground(Color.ORANGE);
                    parkS2[i][j].setText("Disponible");
                } else {
                    parkS2[i][j].setBackground(Color.RED);
                    parkS2[i][j].setText("No disponible");
                }
            }
        }
    }

